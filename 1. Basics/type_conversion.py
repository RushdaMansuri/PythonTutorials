# Type Converison / Type Casting
# convert from one datatype to other

a = "rushda"
b = "mansuri"
print(a + b)

c = "100"
d = "200"
print(c + d)

# str to int conversion
e = int(c) + int(d)
print(e)

# str to float conversion
x = int("100")
y = float("200")
print(x + y)

# float to int conversion
m = int(340.5)  # Lowest value
print(m)
